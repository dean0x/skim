/// Wait for SIGINT (Ctrl-C) or SIGTERM (kill).
///
/// Returns when either signal is received. Used by the accept loop to trigger
/// graceful shutdown (AC23 / AD-PXY-14).
async fn wait_for_shutdown_signal() {
    #[cfg(unix)]
    {
        use tokio::signal::unix::{SignalKind, signal};
        // Registration failure is a fatal misconfiguration (e.g. signal already
        // blocked by a parent process). Return immediately so the caller can
        // trigger shutdown gracefully rather than hanging on a signal that never
        // fires. Production container environments always allow SIGINT/SIGTERM.
        let Ok(mut sigint) = signal(SignalKind::interrupt()) else {
            return;
        };
        let Ok(mut sigterm) = signal(SignalKind::terminate()) else {
            return;
        };
        tokio::select! {
            _ = sigint.recv() => {},
            _ = sigterm.recv() => {},
        }
    }
    #[cfg(not(unix))]
    {
        // Windows: only ctrl_c is available. Failure is non-fatal — return
        // immediately so the caller shuts down gracefully.
        let _ = tokio::signal::ctrl_c().await;
    }
}

