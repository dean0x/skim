/// Shared shutdown flag.
///
/// Set to `true` by the signal handler; polled by the accept loop.
/// Reserved for future use by a public shutdown API (AC23 / AD-PXY-14).
#[allow(dead_code)]
pub(crate) struct ShutdownFlag(Arc<AtomicBool>);

#[allow(dead_code)]
impl ShutdownFlag {
    fn new() -> (Self, Arc<AtomicBool>) {
        let flag = Arc::new(AtomicBool::new(false));
        (Self(Arc::clone(&flag)), flag)
    }

    fn is_set(&self) -> bool {
        self.0.load(Ordering::SeqCst)
    }
}

