#[cfg(test)]
#[allow(clippy::unwrap_used, clippy::expect_used, clippy::panic)]
mod tests {
    use super::*;
    use crate::detect::ProxyProvider;

    // AC13: request IDs are monotonically increasing and not derived from headers.
    #[test]
    fn test_request_id_monotonic() {
        let id1 = next_request_id();
        let id2 = next_request_id();
        assert!(
            id1.starts_with("px-"),
            "request ID must start with px-: {id1}"
        );
        assert!(
            id2.starts_with("px-"),
            "request ID must start with px-: {id2}"
        );
        assert_ne!(id1, id2, "request IDs must be unique");
    }

    // AC13: request ID format never looks like a header value.
    #[test]
    fn test_request_id_not_api_key_shaped() {
        for _ in 0..10 {
            let id = next_request_id();
            // Must not start with known API key prefixes.
            assert!(
                !id.starts_with("sk-ant-"),
                "request ID must not look like an API key: {id}"
            );
            assert!(
                !id.starts_with("Bearer "),
                "request ID must not look like a bearer token: {id}"
            );
        }
    }

    // AC22: json_response helper produces valid responses.
    #[test]
    fn test_json_response_helper() {
        let resp = json_response(StatusCode::OK, r#"{"status":"ok"}"#);
        assert_eq!(resp.status(), StatusCode::OK);
        // Content-type header present.
        let ct = resp
            .headers()
            .get(hyper::header::CONTENT_TYPE)
            .and_then(|v| v.to_str().ok())
            .unwrap_or("");
        assert!(ct.contains("application/json"), "must be application/json");
    }

    // AC9 marker: catch_unwind around analytics should not fail the request.
    // This test directly verifies the pattern used in handle_request.
    #[test]
    fn test_catch_unwind_analytics_panic_does_not_propagate() {
        struct PanicHook;
        impl AnalyticsHook for PanicHook {
            fn on_request(&self, _event: &ProxyEvent) {
                // Intentional panic to test containment.
                // In tests, panic is allowed.
                std::panic::catch_unwind(|| panic!("test panic")).ok();
            }
        }

        let hook = Arc::new(PanicHook);
        let event = ProxyEvent::new(ProxyProvider::Anthropic, 0, 0, Duration::ZERO);

        // This simulates the catch_unwind used in handle_request.
        let result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
            hook.on_request(&event);
        }));
        // Should not propagate — either Ok or caught.
        drop(result);
        // If we reach here, the test passes (no uncaught panic).
    }
}
