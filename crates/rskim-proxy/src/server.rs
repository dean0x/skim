/// Handle a single HTTP request.
///
/// This function is called once per request within a connection task.
/// It performs:
/// 1. Health endpoint short-circuit (livez/readyz/health).
/// 2. Request body buffering (up to max_body_bytes; oversize → passthrough).
/// 3. Provider detection + auth classification.
/// 4. Transform pipeline (identity in #303; #304 injects BlockRouter).
/// 5. Header rewrite (hop-by-hop strip, host rewrite — no Via, AD-PXY-15).
/// 6. Forward to upstream (within upstream_timeout).
/// 7. Stream response body chunk-by-chunk to client.
/// 8. Record readiness outcome + fire analytics hook.
///
/// ## AC13 — request_id is never header-derived
///
/// The request ID is generated by [`next_request_id`]. The incoming
/// `x-request-id` / `x-correlation-id` headers (if any) are forwarded to the
/// upstream byte-identically but are NEVER used as the proxy's own correlation ID.
async fn handle_request(
    req: Request<Incoming>,
    config: Arc<ProxyConfig>,
    pipeline: Arc<TransformPipeline>,
    upstream_client: Arc<UpstreamClient>,
    readiness: Arc<ReadinessState>,
    analytics: Arc<dyn AnalyticsHook>,
) -> Result<Response<ProxyBody>, std::convert::Infallible> {
    let request_id = next_request_id();
    let start = Instant::now();

    // ---- Health short-circuit ----
    let path = req.uri().path().to_owned();
    if is_health_path(&path) {
        let response = match path.as_str() {
            "/livez" => {
                let (status, body) = livez_response();
                json_response(StatusCode::from_u16(status).unwrap_or(StatusCode::OK), body)
            }
            // AC18: no panics on the forwarding path — use a defensive fallback
            // for any is_health_path variant not explicitly matched above.
            // In practice /readyz and /health are the only other values, but
            // defensive matching avoids unreachable! (which would panic).
            _ => {
                let (status, body) = readyz_response(&readiness);
                json_response(
                    StatusCode::from_u16(status).unwrap_or(StatusCode::SERVICE_UNAVAILABLE),
                    body,
                )
            }
        };
        return Ok(response);
    }

    // ---- Collect request parts ----
    let (parts, body) = req.into_parts();

    // Collect headers as (String, String) pairs for HeaderView.
    let header_pairs: Vec<(String, String)> = parts
        .headers
        .iter()
        .filter_map(|(name, value)| {
            let v = value.to_str().ok()?.to_owned();
            Some((name.as_str().to_owned(), v))
        })
        .collect();

    // Classify auth (header-shape only — values never read for logging, AC13).
    let auth_mode = classify_auth(header_pairs.iter().map(|(k, v)| (k.as_str(), v.as_str())));

    // ---- Buffer body (up to max_body_bytes via Limited, AD-PXY-10) ----
    //
    // http_body_util::Limited enforces the max_body_bytes cap. On LengthLimitError
    // (body exceeded the cap) we fall through to a streaming passthrough of the
    // ORIGINAL body (fail-open, AD-PXY-10: oversize requests stream through
    // untransformed). Only the bounded prefix is needed for detection; the pipeline
    // is bypassed for oversize bodies.
    //
    // On read error (client dropped the connection mid-send) we terminate cleanly
    // without forwarding a fabricated empty body (a fabricated empty body would
    // silently corrupt the upstream request — PF-002 / AC4 byte-identity invariant).
    use http_body_util::Limited;
    let limited = Limited::new(body, config.max_body_bytes);
    let body_bytes = match limited.collect().await {
        Ok(collected) => collected.to_bytes(),
        Err(e) => {
            // Distinguish oversize from read error.
            // Limited::collect() returns Err when the body exceeds the cap.
            // We log and fail-open by returning a 413 for oversize, or closing
            // the connection on a read error.
            //
            // For this ticket we treat both as a forward-abort (not fabricate
            // empty body). A genuinely oversize streaming path is deferred to
            // #304 per ADR-001 (the streaming-through path requires the forward
            // layer, not available in isolation here).
            warn!(request_id = %request_id, error = %e, "failed to read request body (oversize or client disconnect) — aborting forward");
            return Ok(json_response(
                StatusCode::BAD_REQUEST,
                r#"{"error":"request body read error"}"#,
            ));
        }
    };
    let request_bytes = body_bytes.len() as u64;

    // ---- Provider detection ----
    let sniff_limit = std::cmp::min(body_bytes.len(), crate::detect::SHAPE_SNIFF_LIMIT);
    let provider = detect_provider(&path, &body_bytes[..sniff_limit]);

    // ---- 502 for Unknown provider with no default upstream (D8 / AC3) ----
    if provider == ProxyProvider::Unknown && config.upstream_default.is_none() {
        warn!(
            request_id = %request_id,
            "Unknown provider and no default upstream configured — returning 502 (D8/AC3)"
        );
        return Ok(json_response(
            StatusCode::BAD_GATEWAY,
            r#"{"error":"no default upstream configured for Unknown provider"}"#,
        ));
    }

    // ---- Transform pipeline ----
    // Bodies that exceed max_body_bytes are streamed through untransformed.
    // (The body was already fully buffered above for the sniff — oversize
    // streams are a forward-path concern; this ticket buffers for sniff only.)
    let header_view = HeaderView::new(&header_pairs);
    let ctx = TransformContext::new(provider.clone(), auth_mode, &request_id, &header_view);

    let null_sink = rskim_contract::log::MockSink::new();

    // AC9: per-transform panic containment — catch_unwind around pipeline.run.
    // A panicking stage falls back to the original body bytes (fail-open).
    // pipeline.run takes Vec<u8>, so convert from Bytes.
    let transformed_body = {
        let body_vec = body_bytes.to_vec();
        let pipeline_result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
            pipeline.run(body_vec, &ctx, &null_sink)
        }));
        match pipeline_result {
            Ok(outcome) => Bytes::from(outcome.bytes),
            Err(_panic) => {
                warn!(request_id = %request_id, "transform stage panicked — using original body (AC9 fail-open)");
                body_bytes.clone()
            }
        }
    };

    // ---- Determine upstream URL ----
    // Map provider to the key used in ProxyConfig::upstream_for.
    // #[non_exhaustive]: the wildcard arm is required for non_exhaustive enums matched
    // outside the defining crate (AC24 / AD-PXY-02). Future variants fall back to
    // the default upstream via an empty key (no per-provider override → upstream_for
    // returns upstream_default).
    #[allow(unreachable_patterns)]
    let provider_key = match &provider {
        ProxyProvider::Anthropic => "anthropic",
        ProxyProvider::OpenAI => "openai",
        _ => "",
    };
    // upstream_for already falls back to upstream_default when provider_key has no entry.
    let upstream_url = config.upstream_for(provider_key).unwrap_or("").to_owned();

    if upstream_url.is_empty() {
        return Ok(json_response(
            StatusCode::BAD_GATEWAY,
            r#"{"error":"no upstream configured"}"#,
        ));
    }

    // ---- Forward request to upstream ----
    // AD-PXY-14: upstream_timeout = 60s (evidence-cited in config.rs).
    let forward_result = timeout(
        config.upstream_timeout,
        forward_request(parts, transformed_body, &upstream_url, &upstream_client),
    )
    .await;

    let upstream_response = match forward_result {
        Err(_timeout) => {
            // Upstream did not respond within the configured timeout → 504 (AC20).
            // Log the actual operative timeout from config (not the default const) so
            // operators who override --upstream-timeout see the correct value.
            readiness.record_failure();
            warn!(
                request_id = %request_id,
                timeout_secs = %config.upstream_timeout.as_secs(),
                "upstream timeout (AC20)"
            );
            return Ok(json_response(
                StatusCode::GATEWAY_TIMEOUT,
                r#"{"error":"upstream timeout","code":"504"}"#,
            ));
        }
        Ok(Err(ForwardError::BadUpstreamUrl(u))) => {
            readiness.record_failure();
            warn!(request_id = %request_id, upstream = %u, "bad upstream URL");
            return Ok(json_response(
                StatusCode::BAD_GATEWAY,
                r#"{"error":"bad upstream URL"}"#,
            ));
        }
        Ok(Err(ForwardError::Upstream(e))) => {
            // Connection refused, TCP reset, etc. → relay as 502 (AC10).
            // Use serde_json to build the body so the upstream error string is
            // correctly JSON-escaped even if it contains quotes, backslashes, or
            // control characters (A03 — escape output for its context).
            readiness.record_failure();
            warn!(request_id = %request_id, error = %e, "upstream connection error (AC10)");
            let body = serde_json::json!({"error": "upstream error", "detail": e}).to_string();
            return Ok(json_response(StatusCode::BAD_GATEWAY, body));
        }
        Ok(Ok(resp)) => resp,
    };

    // ---- Relay upstream response (AC5, AC7) ----
    //
    // AC5: SSE first-event-before-upstream-finish — the response body is streamed
    // frame-by-frame. The client receives each frame as it arrives from upstream,
    // so event 1 reaches the client before upstream closes the stream.
    //
    // AC7: bounded memory on large responses — we do NOT buffer the full response
    // body. The `Incoming` body is forwarded directly as a streaming `BoxBody`.
    // A slow client naturally applies TCP backpressure to the upstream read.
    //
    // Mid-stream upstream disconnect (AC10) is surfaced by hyper as a body-read
    // error on the client side — the stream terminates cleanly rather than hanging.
    let (resp_parts, resp_body) = upstream_response.into_parts();
    let resp_status = resp_parts.status;
    let resp_headers = resp_parts.headers.clone();

    // Record readiness success (upstream responded, even if 5xx).
    readiness.record_success();

    // We do not know the response_bytes at relay time for streaming responses.
    // Use 0 as a sentinel; #305 will add proper byte counting via the analytics
    // event extension (non_exhaustive ProxyEvent).
    let response_bytes: u64 = 0;

    // ---- Build relay response with streaming body ----
    let mut relay_builder = Response::builder().status(resp_status);
    for (name, value) in &resp_headers {
        // Strip hop-by-hop from the response relay as well (AC12).
        if !is_hop_by_hop(name.as_str()) {
            relay_builder = relay_builder.header(name, value);
        }
    }

    let relay_response = relay_builder
        .body(streaming_body(resp_body))
        .unwrap_or_else(|_| {
            json_response(
                StatusCode::BAD_GATEWAY,
                r#"{"error":"response build failed"}"#,
            )
        });

    // ---- Fire analytics (AC15 / AD-PXY-17) ----
    // The call is synchronous — use ChannelAnalyticsHook (or similar non-blocking
    // implementation) to avoid blocking the request path (AC15 / AD-PXY-17).
    // catch_unwind ensures a panicking hook cannot fail the request (AC9).
    let event = ProxyEvent::new(provider, request_bytes, response_bytes, start.elapsed());
    let _ = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
        analytics.on_request(&event);
    }));

    Ok(relay_response)
}

