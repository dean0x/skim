/// Maximum concurrent connections.
///
/// When this cap is reached, new connections wait in the OS TCP backlog
/// (bounded-accept strategy, AD-PXY-13). The cap protects against memory
/// exhaustion from connection floods.
///
/// Evidence: chosen as a generous upper bound for a local AI proxy (AI
/// requests are large, slow, and few — not high-fan-out HTTP/REST traffic).
/// A local proxy on developer hardware with 16GB RAM should safely handle
/// 512 concurrent 64MiB bodies. Oversize bodies stream through, so the
/// per-connection memory is bounded by the transform buffer (64MiB max, but
/// most AI requests are ~10KB).
pub const DEFAULT_CONNECTION_CAP: usize = 512;

// ============================================================================
// Request-ID generation (AC13 — never header-derived)
// ============================================================================

/// Per-process monotonic request ID counter.
///
/// Combined with the process-start nonce to produce collision-unlikely IDs
/// that are never derived from request headers (AC13 / AD-PXY-08).
static REQUEST_ID_COUNTER: AtomicU64 = AtomicU64::new(0);

/// Generate a process-unique request ID.
///
/// Format: `px-{counter}` where counter is monotonically increasing per process.
/// IDs are NOT derived from any request header — this prevents the x-api-key echo
/// anti-pattern (#301 AC12 guard / AC13).
fn next_request_id() -> String {
    let n = REQUEST_ID_COUNTER.fetch_add(1, Ordering::Relaxed);
    format!("px-{n}")
}

