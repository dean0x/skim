//! Tokio runtime, hyper accept loop, graceful shutdown, and per-connection
//! panic containment.
//!
//! ## AD-PXY-12 — Panic containment (AC9)
//!
//! Panic containment operates at two layers:
//!
//! 1. **tokio task isolation**: each `tokio::spawn`'d connection task is isolated
//!    by the tokio runtime — a panic in the task does not terminate the runtime or
//!    the accept loop. This is the load-bearing per-connection containment boundary.
//!
//! 2. **Per-transform call**: the `catch_unwind` in `handle_request` wraps
//!    every `stage.apply()` call inside `TransformPipeline::run` so a panicking
//!    stage falls back to byte-identical passthrough (AC9 / fail-open). The outer
//!    `catch_unwind` around `pipeline.run()` in `handle_request` (server.rs:264)
//!    catches any panic from the entire pipeline call as a second-level guard.
//!    Per-transform-call `catch_unwind` semantics: a panicking stage causes the
//!    WHOLE pipeline output to fall back to original bytes (conservatively safe).
//!
//! Note: there is intentionally NO per-connection `catch_unwind` wrapping the
//! hyper serve-connection `.await`. Wrapping only the connection-future CONSTRUCTOR
//! (not the await) is a no-op — the constructor cannot panic. Runtime task isolation
//! is sufficient; an explicit catch_unwind on a `!Unpin` async future requires
//! additional pinning complexity for no safety gain over tokio's own isolation.
//!
//! ## AD-PXY-13 — Connection cap: bounded TCP backpressure
//!
//! The chosen behavior is **bounded accept**: a semaphore limits the number
//! of concurrently-active connections. When the cap is reached, `accept()`
//! is not called until a connection slot is released — the OS TCP backlog
//! buffers the surplus (AC22). We do NOT return 503; that would require
//! answering the connection, spending resources we're trying to save. The
//! TCP backlog gives the client a natural retry via SO_BACKLOG.
//!
//! Named carve-out: the TCP-backpressure connection-cap behavior is NOT
//! a proxy-originated response. AC4/AC19b byte-identity tests are NOT
//! affected by this path (new connections wait without receiving any response).
//!
//! ## Lifecycle (AD-PXY-14)
//!
//! On SIGINT/SIGTERM:
//! 1. Accept loop stops immediately (no new connections accepted).
//! 2. In-flight connections drain for up to `config.graceful_drain` (5s).
//! 3. Process exits with code 0.
//!
//! Client-disconnect upstream cancel: when a client drops the connection
//! mid-stream, the per-connection task observes the write error and the
//! upstream request is cancelled implicitly via tokio task drop. In #303
//! there is no explicit `timeout(client_disconnect_cancel, …)` wrapper; the
//! 500ms `config.client_disconnect_cancel` bound is stored but not yet wired
//! (wiring is a #304 enhancement per config.rs:189-194).
//!
//! ## Structured logging (AC13)
//!
//! Request IDs are generated by the server layer using a monotonic counter
//! (+ process-unique prefix). They are NEVER derived from request headers
//! (no x-api-key echo / request-id forwarding from client headers).

