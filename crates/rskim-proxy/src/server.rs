/// Start the HTTP proxy server.
///
/// Binds to `config.bind_addr`, accepts connections (bounded by
/// [`DEFAULT_CONNECTION_CAP`]), and handles requests via [`handle_request`].
///
/// Blocks until SIGINT/SIGTERM is received. On signal:
/// 1. Stops accepting new connections.
/// 2. Waits for in-flight connections to drain (up to `config.graceful_drain`).
/// 3. Returns `Ok(())`.
///
/// ## AC23 — Graceful shutdown
///
/// The drain window is `config.graceful_drain` (5s, evidence in config.rs).
/// In-flight streams are given this window; new connections are refused after
/// the signal is received.
///
/// ## AC22 — Connection cap
///
/// A `Semaphore` with [`DEFAULT_CONNECTION_CAP`] permits enforces the cap.
/// When all permits are held, `accept()` blocks (TCP backpressure — AD-PXY-13).
pub async fn run_server(
    config: ProxyConfig,
    pipeline: TransformPipeline,
    analytics: Arc<dyn AnalyticsHook>,
) -> Result<(), ProxyError> {
    let bind_addr = config.bind_addr;
    let config = Arc::new(config);
    let pipeline = Arc::new(pipeline);
    let readiness = ReadinessState::new();
    let upstream_client =
        Arc::new(UpstreamClient::new().map_err(|e| ProxyError::TlsConfig(e.to_string()))?);

    // Bind the listener.
    let listener = TcpListener::bind(bind_addr)
        .await
        .map_err(|e| ProxyError::BindFailed {
            addr: bind_addr.to_string(),
            source: e,
        })?;

    info!(addr = %bind_addr, "proxy listening");

    // Connection-cap semaphore (AD-PXY-13 bounded-accept).
    let connection_cap = Arc::new(Semaphore::new(DEFAULT_CONNECTION_CAP));

    // Shutdown signal handling.
    // We use a simple tokio select! approach: wait for either an accepted
    // connection or a shutdown signal.
    let shutdown = Arc::new(AtomicBool::new(false));
    let shutdown_clone = Arc::clone(&shutdown);

    // Spawn the SIGINT/SIGTERM handler task.
    tokio::spawn(async move {
        wait_for_shutdown_signal().await;
        shutdown_clone.store(true, Ordering::SeqCst);
        info!("shutdown signal received — stopping accept loop");
    });

    // Track in-flight connections for graceful drain.
    let in_flight = Arc::new(AtomicU64::new(0));

    loop {
        // Check shutdown flag before accepting.
        if shutdown.load(Ordering::SeqCst) {
            info!("shutdown: stopping accept loop");
            break;
        }

        // Acquire a connection slot (bounded-accept, AD-PXY-13).
        // acquire_owned().await parks the task until a permit is available — no
        // busy-loop or CPU-spin. The OS TCP backlog buffers surplus connections
        // while we wait. The shutdown flag is re-checked each loop iteration (the
        // `if shutdown` guard at the top of the loop handles clean exit).
        let permit = match connection_cap.clone().acquire_owned().await {
            Ok(p) => p,
            Err(_) => break, // semaphore closed (not expected in normal flow)
        };

        // Accept with a short timeout so we can check the shutdown flag.
        let accept_result = timeout(Duration::from_millis(100), listener.accept()).await;

        match accept_result {
            Err(_timeout) => {
                // No incoming connection in 100ms — loop back to check shutdown.
                drop(permit);
                continue;
            }
            Ok(Err(e)) => {
                warn!(error = %e, "accept error");
                drop(permit);
                continue;
            }
            Ok(Ok((stream, peer_addr))) => {
                let config = Arc::clone(&config);
                let pipeline = Arc::clone(&pipeline);
                let upstream_client = Arc::clone(&upstream_client);
                let readiness = Arc::clone(&readiness);
                let analytics = Arc::clone(&analytics);
                let in_flight = Arc::clone(&in_flight);

                in_flight.fetch_add(1, Ordering::Relaxed);

                // Per-connection task (AD-PXY-12 / AC9).
                //
                // Panic containment: tokio task isolation is the per-connection
                // boundary. A panic in the spawned task terminates only that task;
                // the runtime and the accept loop continue. The per-transform
                // catch_unwind in handle_request (around pipeline.run) is the
                // per-transform boundary. Wrapping `conn.await` in catch_unwind
                // is non-trivial for !Unpin futures and provides no additional
                // safety over tokio's own task isolation.
                tokio::spawn(async move {
                    let _permit = permit; // drops when task finishes (releases slot)
                    // Decrement in-flight counter when the task finishes.
                    // Use a simple RAII wrapper to ensure the counter is decremented
                    // even if the async block exits unexpectedly.
                    struct InFlightGuard(Arc<AtomicU64>);
                    impl Drop for InFlightGuard {
                        fn drop(&mut self) {
                            self.0.fetch_sub(1, Ordering::Relaxed);
                        }
                    }
                    let _in_flight_guard = InFlightGuard(Arc::clone(&in_flight));

                    let io = TokioIo::new(stream);
                    let service = service_fn(move |req| {
                        handle_request(
                            req,
                            Arc::clone(&config),
                            Arc::clone(&pipeline),
                            Arc::clone(&upstream_client),
                            Arc::clone(&readiness),
                            Arc::clone(&analytics),
                        )
                    });

                    let conn = http1::Builder::new().serve_connection(io, service);
                    if let Err(e) = conn.await {
                        // Client disconnect or protocol error — log and continue.
                        // The tokio runtime survives task-level panics (AC9).
                        tracing::debug!(
                            peer = %peer_addr,
                            error = %e,
                            "connection error (client disconnect or protocol)"
                        );
                    }
                });
            }
        }
    }

    // Graceful drain (AC23 / AD-PXY-14).
    info!(
        drain_secs = %config.graceful_drain.as_secs(),
        "draining in-flight connections"
    );
    let drain_deadline = Instant::now() + config.graceful_drain;
    while in_flight.load(Ordering::Relaxed) > 0 && Instant::now() < drain_deadline {
        tokio::time::sleep(Duration::from_millis(50)).await;
    }

    let remaining = in_flight.load(Ordering::Relaxed);
    if remaining > 0 {
        warn!(remaining, "graceful drain timeout — forcing exit");
    } else {
        info!("graceful drain complete");
    }

    Ok(())
}

